int	ft_atoi(const char *str)
{
	int	nbr;
	int	i;
	int	neg;

	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	nbr = 0;
	neg = 0;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = 1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nbr = nbr * 10 + str[i] - 48;
		i++;
	}
	if (neg)
	{
		nbr *= -1;
	}
	return (nbr);
}
